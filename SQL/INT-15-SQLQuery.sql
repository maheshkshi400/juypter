
--  TO CALCULATE BIWEEKLY FRIDAY DATES IN THE YEAR

SELECT DATENAME(WEEKDAY,'2020-12-04'),DATEPART(WEEKDAY,'2020-12-04')


SELECT CASE WHEN
DATEPART(WEEKDAY,'2022-01-01') >6 THEN
DATEADD(DAY,(6-DATEPART(WEEKDAY,'2022-01-01'))+7,'2022-1-01')
ELSE 
DATEADD(DAY,6-DATEPART(WEEKDAY,'2022-01-01'),'2022-1-01') END


SELECT * FROM master..spt_values WHERE TYPE= 'P'

DECLARE @FF DATE=
CASE WHEN
DATEPART(WEEKDAY,'2022-01-01') >6 THEN
DATEADD(DAY,(6-DATEPART(WEEKDAY,'2022-01-01'))+7,'2022-1-01')
ELSE 
DATEADD(DAY,6-DATEPART(WEEKDAY,'2022-01-01'),'2022-1-01') END


SELECT DATEADD(DAY,NUMBER,@FF) AS BIWEEKLY_FRIDAY FROM master..spt_values
WHERE TYPE='P' AND NUMBER%14=0 AND YEAR(DATEADD(DAY,NUMBER,@FF))= 2022