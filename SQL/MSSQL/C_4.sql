--How to delete duplicates from a table in SQL Server

--1. Using a Common Table Expression (CTE)

WITH DELETE_CTE AS
(
SELECT EMP2.*, ROW_NUMBER() OVER(PARTITION BY EMPNO ORDER BY EMPNO) AS RN
FROM EMPLOYEES.dbo.EMP2
)
DELETE FROM DELETE_CTE WHERE RN > 1;



