--WRITE AN QUERY FIND THOSE EMPLOYEES WHOSE SALARY IS MORE THAN THEIR MANAGERS
USE EMPLOYEES;

SELECT * FROM DBO.EMP2;

SELECT E1.EMPNO, E1.ENAME, E1.SAL, E1.MGR, E2.ENAME, E2.SAL
FROM DBO.EMP2 E1 INNER JOIN DBO.EMP2 E2 ON E1.MGR = E2.EMPNO
WHERE E1.SAL > E2.SAL;


SELECT E1.EMPNO, E1.ENAME, E1.SAL, E1.MGR, E2.ENAME, E2.SAL
FROM DBO.EMP2 E1 INNER JOIN DBO.EMP2 E2 ON E1.MGR = E2.EMPNO
WHERE E1.SAL > E2.SAL;