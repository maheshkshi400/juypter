SELECT * FROM EMP5 WHERE SAL=(SELECT MAX(SAL) FROM EMP5 WHERE SAL <(SELECT MAX(SAL) FROM EMP5))


SELECT TOP 2 SAL FROM(
    SELECT DISTINCT TOP 5 SAL,DEPTNO
    FROM EMP5 ORDER BY SAL DESC)
    AS RESULT ORDER BY SAL DESC;


